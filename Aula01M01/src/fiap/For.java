package fiap;

public class For {
    public static void main(String[] args) {
        /* FOR
        * Estrutura de repetição controlada por uma váriavel de contador
        * Sintaxe básica:
        * for (<inicialização>;<condição lógica>;<incremento ou decremento>)
        *       { <Instruções>
        * }
        * for (int i = 0; i < ; i++) {
        *
        *  }
        */

        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
